// ── Helper: Get CQ collection by subject ──

export function getCQBySubject(subject: string): CQCollection | undefined {
  return CQ_COLLECTIONS.find((c) => c.subject === subject);
}

// ── Helper: Get CQs by chapter ──

export function getCQsByChapter(subject: string, chapter: number): CreativeQuestion[] {
  const collection = getCQBySubject(subject);
  if (!collection) return [];
  return collection.questions.filter((q) => q.chapter === chapter);
}

// ── Helper: Get all CQ questions flat ──

export function getAllCQs(): { question: CreativeQuestion; subject: string; subjectBn: string }[] {
  return CQ_COLLECTIONS.flatMap((c) =>
    c.questions.map((q) => ({ question: q, subject: c.subject, subjectBn: c.subjectBn }))
  );
}

// ── Helper: Get chapter name ──

export function getChapterName(subject: string, chapter: number): string {
  return CHAPTER_NAMES[subject]?.[chapter] || `অধ্যায় ${chapter}`;
}
